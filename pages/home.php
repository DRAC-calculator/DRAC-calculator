<p>DRAC is a <b>D</b>ose <b>R</b>ate and <b>A</b>ge <b>C</b>alculator which has been designed to calculate environmental dose rates (Ḋ) and ages for trapped charge dating applications. The calculations are applicable to both optically stimulated luminescence (OSL) and thermoluminescence (TL) dating and may also be useful in some electron spin resonance (ESR) applications. DRAC provides a standardised Ḋ calculator with transparent calculation using published input variables. It is an effective means of removing the potential for miscalculation, allowing improved assessment of Ḋ calculations and simpler inter-laboratory Ḋ comparisons.</p>

<p>The rationale of this DRAC project is to:
	<ul>
		<li>Facilitate inter-laboratory comparisons of Ḋ determinations.</li>
		<li>Reduce the potential of mathematical error in the Ḋ calculation process. </li>
		<li>Allow easy and controlled sensitivity testing of the impact of different using Ḋ parameters on final Ḋ determinations. </li>
		<li>Represent an ongoing collation of datasets and resources. </li>
		<li>Be a resource for students, teachers and newcomers to the discipline. </li>
	</ul>
</p>

<p>DRAC aims to:
	<ul>
		<li>Provide Ḋ calculation for quartz, feldspar and polymineral samples, over a grain size range of 1 – 1000 μm </li>
		<li>Be easy to use and provide a clear calculation process, with minimum assumptions and maximum user flexibility. </li>
		<li>Be an open access research tool, which is continually updated with the latest datasets and tools. </li>
		<li>Stimulate further Ḋ research. </li>
	</ul>
</p>

<p>DRAC has been designed and developed in equal parts by <a href="<?php DRAC_URL ?>?show=whoweare">Julie Durcan</a> and <a href="<?php DRAC_URL ?>?show=whoweare">Georgina King</a>. We welcome feedback and suggestions from the trapped charge dating community, as well as data contributions to the site and collaboration.</p>

